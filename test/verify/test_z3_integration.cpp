TEST(Z3Integration, SiMBA_E3_AffineFromNotX) {
    // 18446744073709502242*~x + 3735887311*x == 49374 + 3735936685*x
    // Because ~x = -1-x, the NOT folds into affine coefficients.
    auto text = run_and_verify(
        { "18446744073709502242*~x + 3735887311*x", "49374 + 3735936685*x", 64 }
    );
    EXPECT_EQ(text, "49374 + 3735936685 * x");
}

