/// Integration tests for the Z3 verification pipeline using MBA expressions
/// sourced from established academic tools.
///
/// Sources:
///   - DenuvoSoftwareSolutions/SiMBA (example-expressions.txt, data/*.txt)
///     "Efficient Deobfuscation of kLinear Mixed Boolean-Arithmetic Expressions"
///     Reichenwallner et al., 2022
///     https://github.com/DenuvoSoftwareSolutions/SiMBA

#include "ExprParser.h"
#include "cobra/core/CoeffInterpolator.h"
#include "cobra/core/Simplifier.h"
#include "cobra/verify/Z3Verifier.h"
#include <gtest/gtest.h>
#include <string>

using namespace cobra;

namespace {

    struct MBATestCase
    {
        std::string obfuscated;
        std::string expected;
        uint32_t bitwidth;
    };

    // Run the full pipeline: parse → simplify → Z3 verify.
    // Returns the simplified expression string.
    std::string run_and_verify(const MBATestCase &tc) {
        auto parsed = ParseAndEvaluate(tc.obfuscated, tc.bitwidth);
        EXPECT_TRUE(parsed.has_value())
            << "Parse failed for: " << tc.obfuscated << "\n  Error: " << parsed.error().message;
        if (!parsed.has_value()) { return ""; }

        Options opts{
            .bitwidth   = tc.bitwidth,
            .max_vars   = 12,
            .spot_check = true,
        };
        auto result = Simplify(parsed.value().sig, parsed.value().vars, nullptr, opts);
        EXPECT_TRUE(result.has_value()) << "Simplify failed for: " << tc.obfuscated
                                        << "\n  Error: " << result.error().message;
        if (!result.has_value()) { return ""; }

        auto num_vars = static_cast< uint32_t >(result.value().real_vars.size());
        auto cob_coeffs =
            InterpolateCoefficients(result.value().sig_vector, num_vars, tc.bitwidth);

        auto z3r = Z3Verify(
            cob_coeffs, *result.value().expr, result.value().real_vars, num_vars, tc.bitwidth
        );
        EXPECT_TRUE(z3r.equivalent) << "Z3 verification failed for: " << tc.obfuscated
                                    << "\n  Counterexample: " << z3r.counterexample;

        return Render(*result.value().expr, result.value().real_vars, tc.bitwidth);
    }

} // namespace

