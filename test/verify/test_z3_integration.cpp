// SiMBA example-expressions.txt — hand-picked from the paper
// ---------------------------------------------------------------------------

TEST(Z3Integration, SiMBA_XAndY_Plus_XOrY) {
    // (x&y)+(x|y) == x+y
    auto text = run_and_verify({ "(x&y)+(x|y)", "x + y", 64 });
    EXPECT_EQ(text, "x + y");
}

TEST(Z3Integration, SiMBA_SingleVarRecovery) {
    // 2*(s&~t)+2*(s^t)-(s|t)+2*~(s^t)-~t-~(s&t) == s
    auto text = run_and_verify({ "2*(s&~t)+2*(s^t)-(s|t)+2*~(s^t)-~t-~(s&t)", "s", 64 });
    EXPECT_EQ(text, "s");
}

